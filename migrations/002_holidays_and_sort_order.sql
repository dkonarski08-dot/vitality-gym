ALTER TABLE employees ADD COLUMN IF NOT EXISTS sort_order integer DEFAULT 999;

-- Set initial sort order based on current alphabetical order
WITH ranked AS (
  SELECT id, ROW_NUMBER() OVER (ORDER BY name) * 10 AS rn
  FROM employees
)
UPDATE employees SET sort_order = ranked.rn
FROM ranked
WHERE employees.id = ranked.id;

